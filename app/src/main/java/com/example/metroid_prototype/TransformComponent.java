package com.example.metroid_prototype;

public class TransformComponent {
    private int xPos = 0;
    private int yPos = 0;

    public int getXPos() {
        return this.xPos;
    }

    public int getYPos() {
        return this.yPos;
    }

    public void setXPos(int xPos) {
        this.xPos = xPos;
    }

    public void setYPos(int yPos) {
        this.yPos = yPos;
    }
}
